    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int codigo = Integer.parseInt(jComboBox1.getSelectedItem().toString());
	
	DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
	List<Producto> productos = controladorProducto.productosPorBodega(codigo);
        tabla.setRowCount(0);
	for(int i = 0; i < productos.size(); i++){
	    tabla.addRow(new Object[]{
		productos.get(i).getId(),
		productos.get(i).getNombre().trim(),
		productos.get(i).getDescripcion().trim(),
		productos.get(i).getStock(),
		productos.get(i).getPrecio(),
		productos.get(i).getBodega().getCodigo()
	    });
	}
    }//GEN-LAST:event_btnBuscarActionPerformed


